<template>
  <div :class="handleClass" :style="handleStyle" @click="onClickHandle">
    <slot v-if="theme !== 'tag'">
      <span v-show="!value.length" class="zg-select-label zg-placeholder">{{placeholder}}</span>
      <span v-show="value.length" class="zg-select-label">{{resultLabel}}</span>
      <span v-if="theme === 'noborder'" class="zg-count" v-show="value.length > 1">({{value.length}})</span>
      <i :class="arrowIcon"></i>
    </slot>
    <template v-else>
      <span v-show="!value.length && !focus" class="zg-placeholder">{{placeholder}}</span>
      <zg-tag v-for="item in value"
              :key="item[keyField]" closeable @close="onDel(item)">
        {{item[aliasField] || item[labelField]}}
      </zg-tag>
      <input type="text"
             :style="inputStyle"
             @keyup.enter="onEnter"
             @keydown.delete="onDelete"
             @keyup.up="onPre"
             @keyup.left="onPre"
             @keyup.down="onNext"
             @keyup.right="onNext"
             @focus="focus = true"
             @blur="focus = false"
             v-model="search"
             ref="input"/>
      <span class="zg-temp" ref="search">{{search}}</span>
    </template>
  </div>
</template>

<script>
  import ZgTag from '../tag/tag'

  export default {
    components: {ZgTag},
    name: 'zgSelectorHandle',
    props: {
      /**
       * @description 已选列表
       */
      value: {
        type: Array,
        default () {
          return []
        }
      },
      /**
       * @description 支持normal、noborder、tag
       */
      theme: {
        type: String,
        default: 'normal'
      },
      /**
       * @description placeholder
       */
      placeholder: {
        type: String,
        default: '请选择'
      },
      /**
       * @description 用于展示的字段名称
       */
      labelField: {
        type: String,
        required: true
      },
      /**
       * @description 别名字段，设置别名后，优先展示别名
       */
      aliasField: {
        type: String
      },
      /**
       * @description 选项唯一标识字段
       */
      keyField: {
        type: String,
        required: true
      },
      /**
       * @description 下拉框宽度。如果theme为noborder，则该值为最大宽度，组件宽度在范围内自适应
       */
      width: {
        type: Number,
        default: 150
      },
      /**
       * @description 下拉框尺寸
       */
      size: {
        type: String,
        default: 'normal',
        validator (value) {
          return ['small', 'medium', 'big', 'normal'].includes(value)
        }
      },
      /**
       * @description 禁用下拉框
       */
      disable: {
        type: Boolean,
        default: false
      },
      /**
       * @description 下拉框是否处于选中状态
       */
      active: {
        type: Boolean,
        default: false
      }
    },
    data () {
      return {
        search: '',
        inputWidth: 15,
        focus: false
      }
    },
    computed: {
      handleClass () {
        let clazz = {
          'zg-select-handle': true,
          active: this.active,
          disable: this.disable
        }
        clazz[`zg-theme-${this.theme}`] = true
        clazz['zg-size-' + this.size] = true
        return clazz
      },
      handleStyle () {
        let style = {}
        if (this.theme === 'normal') {
          style.width = this.width + 'px'
        } else {
          style.maxWidth = this.width + 'px'
        }
        return style
      },
      arrowIcon () {
        return {
          'zg-select-arrow': true,
          'zgicon-down': this.theme === 'normal',
          'zgicon-pulldown': this.theme === 'noborder'
        }
      },
      resultLabel () {
        return this.value.map(option => {
          return option[this.aliasField] || option[this.labelField]
        }).join(',')
      },
      inputStyle () {
        return {
          width: this.inputWidth + 'px'
        }
      }
    },
    watch: {
      search (val) {
        this.$nextTick(() => {
          this.inputWidth = Math.max(this.$refs.search.offsetWidth, 10) + 5
        })
        this.$emit('search', val)
      }
    },
    methods: {
      /**
       * @description handle点击处理
       */
      onClickHandle () {
        if (this.theme === 'tag') {
          this.$refs.input.focus()
        }
        this.$emit('click')
      },
      /**
       * @description 删除键处理
       * @param data
       */
      onDel (data) {
        this.$emit('delete', data)
      },
      /**
       * @description 回车处理，如果搜索内容在store中，没有完全匹配的，则
       */
      onEnter () {
        let item = {
          temp: true // 表明选项为临时选项，删除时，从store中彻底删除
        }
        item[this.keyField] = this.search
        item[this.labelField] = this.search
        this.$emit('enter', this.search ? item : null)
        this.search = ''
      },
      onDelete () {
        if (!this.search && this.value.length) {
          this.onDel(this.value[this.value.length - 1])
        }
      },
      onPre () {
        this.$emit('pre')
      },
      onNext () {
        this.$emit('next')
      }
    }
  }
</script>

<style lang="sass">
